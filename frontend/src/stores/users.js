import { defineStore } from 'pinia'
import { usersApi } from '@/api/users'

export const useUsersStore = defineStore('users', {
  state: () => ({
    users: [],
    currentUser: null,
    roles: [],
    isLoading: false,
    error: null,
    pagination: {
      total: 0,
      page: 1,
      perPage: 20
    }
  }),

  getters: {
    activeUsers: (state) => state.users.filter(u => u.is_active),
    inactiveUsers: (state) => state.users.filter(u => !u.is_active),
    readers: (state) => state.users.filter(u => u.roles?.some(r => r.name === 'reader')),
    librarians: (state) => state.users.filter(u => u.roles?.some(r => r.name === 'librarian')),
    admins: (state) => state.users.filter(u => u.roles?.some(r => r.name === 'admin'))
  },

  actions: {
    async fetchUsers(params = {}) {
      this.isLoading = true
      this.error = null
      
      try {
        const response = await usersApi.getAll({
          skip: (this.pagination.page - 1) * this.pagination.perPage,
          limit: this.pagination.perPage,
          ...params
        })
        this.users = response.data
        this.pagination.total = parseInt(response.headers?.['x-total-count']) || this.users.length
        return this.users
      } catch (error) {
        this.error = error.response?.data?.detail || '???????????? ???????????????? ??????????????????????????'
        throw error
      } finally {
        this.isLoading = false
      }
    },

    async fetchUserById(id) {
      this.isLoading = true
      this.error = null
      
      try {
        const response = await usersApi.getById(id)
        this.currentUser = response.data
        return this.currentUser
      } catch (error) {
        this.error = error.response?.data?.detail || '???????????? ???????????????? ????????????????????????'
        throw error
      } finally {
        this.isLoading = false
      }
    },

    async fetchCurrentUser() {
      this.isLoading = true
      this.error = null
      
      try {
        const response = await usersApi.getMe()
        this.currentUser = response.data
        return this.currentUser
      } catch (error) {
        this.error = error.response?.data?.detail || '???????????? ???????????????? ???????????????? ????????????????????????'
        throw error
      } finally {
        this.isLoading = false
      }
    },

    async searchUsers(query) {
      this.isLoading = true
      this.error = null
      
      try {
        const response = await usersApi.search(query)
        return response.data
      } catch (error) {
        this.error = error.response?.data?.detail || '???????????? ???????????? ??????????????????????????'
        throw error
      } finally {
        this.isLoading = false
      }
    },

    async createUser(userData) {
      this.isLoading = true
      this.error = null
      
      try {
        const response = await usersApi.create(userData)
        await this.fetchUsers()
        return response.data
      } catch (error) {
        this.error = error.response?.data?.detail || '???????????? ???????????????? ????????????????????????'
        throw error
      } finally {
        this.isLoading = false
      }
    },

    async updateUser(id, userData) {
      this.isLoading = true
      this.error = null
      
      try {
        const response = await usersApi.update(id, userData)
        if (this.currentUser?.id === id) {
          this.currentUser = response.data
        }
        await this.fetchUsers()
        return response.data
      } catch (error) {
        this.error = error.response?.data?.detail || '???????????? ???????????????????? ????????????????????????'
        throw error
      } finally {
        this.isLoading = false
      }
    },

    async deleteUser(id) {
      this.isLoading = true
      this.error = null
      
      try {
        await usersApi.delete(id)
        await this.fetchUsers()
        if (this.currentUser?.id === id) {
          this.currentUser = null
        }
      } catch (error) {
        this.error = error.response?.data?.detail || '???????????? ???????????????? ????????????????????????'
        throw error
      } finally {
        this.isLoading = false
      }
    },

    async changePassword(id, passwordData) {
      this.isLoading = true
      this.error = null
      
      try {
        const response = await usersApi.changePassword(id, passwordData)
        return response.data
      } catch (error) {
        this.error = error.response?.data?.detail || '???????????? ?????????? ????????????'
        throw error
      } finally {
        this.isLoading = false
      }
    },

    async fetchRoles() {
      this.isLoading = true
      this.error = null
      
      try {
        // ???????????????? ???????????????????? ?????????????????????? ????????, ???????? ?????? API
        // ?? ???????????????? ???????????????????? ?????????? ???????????????? ???????????????? ?????? ?????????????????? ??????????
        this.roles = [
          { id: 1, name: 'reader', description: '?????????????? ????????????????' },
          { id: 2, name: 'librarian', description: '????????????????????????' },
          { id: 3, name: 'admin', description: '?????????????????????????? ??????????????' }
        ]
        return this.roles
      } catch (error) {
        this.error = error.response?.data?.detail || '???????????? ???????????????? ??????????'
        throw error
      } finally {
        this.isLoading = false
      }
    },

    async addRole(userId, roleName) {
      this.isLoading = true
      this.error = null
      
      try {
        const response = await usersApi.addRole(userId, {
          role_name: roleName,
          action: 'add'
        })
        return response.data
      } catch (error) {
        this.error = error.response?.data?.detail || '???????????? ???????????????????? ????????'
        throw error
      } finally {
        this.isLoading = false
      }
    },

    async removeRole(userId, roleName) {
      this.isLoading = true
      this.error = null
      
      try {
        const response = await usersApi.addRole(userId, {
          role_name: roleName,
          action: 'remove'
        })
        return response.data
      } catch (error) {
        this.error = error.response?.data?.detail || '???????????? ???????????????? ????????'
        throw error
      } finally {
        this.isLoading = false
      }
    },

    async toggleUserActive(userId) {
      this.isLoading = true
      this.error = null
      
      try {
        const response = await usersApi.toggleActive(userId)
        
        // ?????????????????? ???????????????????????? ?? ????????????
        const index = this.users.findIndex(u => u.id === userId)
        if (index !== -1) {
          this.users[index] = response.data
        }
        
        // ?????????????????? ???????????????? ????????????????????????, ???????? ?????? ????
        if (this.currentUser?.id === userId) {
          this.currentUser = response.data
        }
        
        return response.data
      } catch (error) {
        this.error = error.response?.data?.detail || '???????????? ?????????????????? ??????????????'
        throw error
      } finally {
        this.isLoading = false
      }
    },

    setPagination(page, perPage) {
      this.pagination.page = page
      this.pagination.perPage = perPage
    },

    clearCurrentUser() {
      this.currentUser = null
    },

    clearError() {
      this.error = null
    }
  }
})